function aboutMe() {
    var x = document.getElementById("aboutMe");
    if (x.style.display === "none") {
      x.style.display = "block";
    } else {
      x.style.display = "none";
    }
  }

  function resume() {
    var x = document.getElementById("resume");
    if (x.style.display === "none") {
      x.style.display = "block";
    } else {
      x.style.display = "none";
    }
  }